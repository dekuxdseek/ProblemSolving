{
    vi fact;
    while(n%2 == 0)
    {
        fact.pb(2);
        n/=2;

    }

    for(int i =3 ; i*i<=n; i += 2)
    {
        while(n%i ==0 && prime[i])
        {
            fact.pb(i);
            n/=i;
        }
    }
    if(n>1)fact.pb(n);
    return fact;
}
